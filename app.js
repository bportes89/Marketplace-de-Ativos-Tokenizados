      const contract = new web3.eth.Contract(contractABI, contractAddress);

    async function connectMetaMask() {
    if (window.ethereum) {
        try {
            // Solicita acesso à conta do usuário
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            console.log("MetaMask conectada");
            await updateTokenBalance(); // Atualiza o saldo após a conexão
            await listAvailableTokens(); // Lista os tokens disponíveis após a conexão
        } catch (error) {
            console.error("Usuário rejeitou a solicitação de conexão:", error);
        }
    } else {
        console.error("MetaMask não está instalada!");
    }
}

    async function updateTokenBalance() {
    const accounts = await web3.eth.getAccounts();
    const balance = await contract.methods.balanceOf(accounts[0]).call();
    document.getElementById('tokenBalance').innerText = balance;
}

    async function listAvailableTokens() {
    // Exemplo de tokens disponíveis com imagens
    const tokens = [
        { name: "MyToken", symbol: "MTK", price: "1.00", img: "https://via.placeholder.com/50" },
        { name: "Token Exemplo", symbol: "TEX", price: "0.50", img: "https://via.placeholder.com/50" }
    ];

    const tokenList = document.getElementById('tokenList');
    tokens.forEach(token => {
        const li = document.createElement('li');
        li.className = 'token-item';
        li.innerHTML = `<img src="${token.img}" alt="${token.name}"> ${token.name} (${token.symbol}) - Preço: $${token.price}`;
        tokenList.appendChild(li);
    });
}

    async function recordTransaction(transaction) {
    const transactionList = document.getElementById('transactionList');
    const li = document.createElement('li');
    li.innerText = transaction;
    transactionList.appendChild(li);
}

    document.getElementById('connectWallet').onclick = connectMetaMask; // Conectar MetaMask ao clicar no botão

    document.getElementById('buyToken').onclick = async () => {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.transfer(accounts[1], 100).send({ from: accounts[0] });
    alert('Tokens comprados com sucesso!');
    updateTokenBalance(); // Atualiza o saldo após a compra
    recordTransaction(`Comprou 100 MTK de ${accounts[0]} para ${accounts[1]} em ${new Date().toLocaleString()}`);
};

